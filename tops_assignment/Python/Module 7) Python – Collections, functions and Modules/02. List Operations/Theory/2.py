# Understanding list methods like append(), insert(), remove(), pop().

# append
print("The append method in Python is used to add a single item to the end of list. This method modifies the original list and does not return a new list.")

# insert
print("Python List insert method inserts an item at a specific index in a list.")

# remove
print("Python list remove function removes the first occurrence of a given item from list. It make changes to the current list. It only takes one argument, element we want to remove and if that element is not present in the list, it gives ValueError.")

# pop
print("The pop() method is used to remove an element from a list at a specified index and return that element. If no index is provided, it will remove and return the last element by default.")