# Write a Python program to create a calculator using functions.

a = int(input("Enter A : "))
b = int(input("Enter B : "))

print ('Sum: ', a + b)  

print ('Subtraction: ', a - b)   

print ('Multiplication: ', a * b)  

print ('Division: ', a / b) 

print ('Floor Division: ', a // b)

print ('Modulo: ', a % b)  

print ('Power: ', a ** b)   