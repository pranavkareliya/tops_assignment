# Python operators: arithmetic, comparison, logical, bitwise.

# arithmetic
print("+ 	Addition")
print("- 	Subtraction")
print("* 	Multiplication")
print("/ 	Division")
print("% 	Modulus")
print("** 	Exponentiation")
print("// 	Floor division")

# comparison
print("== 	Equal")
print("!= 	Not equal")
print("> 	Greater than")
print("< 	Less than")
print(">= 	Greater than or equal to")
print("<= 	Less than or equal to")

# logical
print("and")
print("or")
print("not")

# bitwise
print("&    AND")
print("|    OR")
print("~    NOT")